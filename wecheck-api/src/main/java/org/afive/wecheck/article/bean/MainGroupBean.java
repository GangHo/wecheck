package org.afive.wecheck.article.bean;

public class MainGroupBean {
	private int mainGroupID;
	private String insertedTime;
	private int order;
	private int articleGroupID;
	public int getMainGroupID() {
		return mainGroupID;
	}
	public void setMainGroupID(int mainGroupID) {
		this.mainGroupID = mainGroupID;
	}
	public String getInsertedTime() {
		return insertedTime;
	}
	public void setInsertedTime(String insertedTime) {
		this.insertedTime = insertedTime;
	}
	public int getOrder() {
		return order;
	}
	public void setOrder(int order) {
		this.order = order;
	}
	public int getArticleGroupID() {
		return articleGroupID;
	}
	public void setArticleGroupID(int articleGroupID) {
		this.articleGroupID = articleGroupID;
	}
	
	
}
