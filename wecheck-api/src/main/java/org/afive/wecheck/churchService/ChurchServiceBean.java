package org.afive.wecheck.churchService;

public class ChurchServiceBean {
	private int churchServiceID;
	private int regionID;
	private int unitID;
	private int state;
	private int adminUserID;
	private String startTime;
	private String endTime;
	private String registeredTime;
	
	public int getChurchServiceID() {
		return churchServiceID;
	}
	public void setChurchServiceID(int churchServiceID) {
		this.churchServiceID = churchServiceID;
	}
	public int getRegionID() {
		return regionID;
	}
	public void setRegionID(int regionID) {
		this.regionID = regionID;
	}
	public int getUnitID() {
		return unitID;
	}
	public void setUnitID(int unitID) {
		this.unitID = unitID;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public int getAdminUserID() {
		return adminUserID;
	}
	public void setAdminUserID(int adminUserID) {
		this.adminUserID = adminUserID;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getRegisteredTime() {
		return registeredTime;
	}
	public void setRegisteredTime(String registeredTime) {
		this.registeredTime = registeredTime;
	}
	
	
	
	
	
}
